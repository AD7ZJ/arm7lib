/**
 *  Convert a binary block to ASCII data using the Base 64 encoding convention.
 */
void Base64Encoder::Encode (const void *binary, char *text, uint32_t length)
{
    uint32_t data;
    const uint8_t *encode;

    encode = reinterpret_cast<const uint8_t *> (binary);

    while (length-- != 0)
    {
        // Encode byte 1.
        data = *encode++;
        *text++ = B64EncodeTable[data >> 2];

        // Encode byte 2.
        data = (data << 8) & 0x300;

        if (length-- != 0)
            data |= *encode++;

        *text++ = B64EncodeTable[data >> 4];

        // Encode byte 3.
        data = (data << 8) & 0xf00;

        if (length-- != 0)
            data |= *encode++;

        *text++ = B64EncodeTable[data >> 6];

        // Encode byte 4.
        *text++ = B64EncodeTable[data & 0x3f];
    } // END while

    *text = 0;
}

/**
 *  Convert an ASCII data string to a binary block using the Base 64 encoding convention.
 */
void Base64Encoder::Decode (const char *text, void *binary)
{
    uint32_t data;
    uint8_t *decode;

    decode = reinterpret_cast<uint8_t *> (binary);

    while (*text != 0)
    {
        data = B64DecoderTable[static_cast<uint32_t> (*text++)] << 18;

        if (*text != 0)
            data |= B64DecoderTable[static_cast<uint32_t> (*text++)] << 12;

        if (*text != 0)
            data |= B64DecoderTable[static_cast<uint32_t> (*text++)] << 6;

        if (*text != 0)
            data |= B64DecoderTable[static_cast<uint32_t> (*text++)];

        *decode++ = data >> 16;
        *decode++ = (data >> 8) & 0x00ff;
        *decode++ = data & 0x00ff;
    } // END while
}
